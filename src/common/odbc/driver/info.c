        else                                 s=&pdbc->db_delimited_case;
        if (rgbInfoValue)
           { i2temp = (i2)((CMcmpcase(s,"U") == 0) ? SQL_IC_UPPER :
                           (CMcmpcase(s,"M") == 0) ? SQL_IC_MIXED :
                                              SQL_IC_LOWER);
             I2ASSIGN_MACRO(i2temp, *rgbInfoValue);
           }
        if (pcbInfoValue)
             I2ASSIGN_MACRO(i2sizeofSWORD, *pcbInfoValue);
        UnlockDbc (pdbc);
