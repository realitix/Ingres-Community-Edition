	/*
	** Name: SqlBool
	**
	** Description:
	**	Class which represents an SQL Boolean value.  
	**
	**	Supports conversion to byte, short, integer, long, float
	**	double, BigDecimal, String and Object.
	**
	**	The data value accessor methods do not check for the NULL
	**	condition.  The super-class isNull() method should first
	**	be checked prior to calling any of the accessor methods.
	**
	**  Public Methods:
	**
	**	Set			Assign a new non-NULL data value.
	**	getBoolean		Data value accessor methods
	**	getByte
	**	getShort
	**	getInt
	**	getLong
	**	getFloat
	**	getDouble
	**	getBigDecimal
	**	getString
	**	getObject
	**
	**  Private Data:
	**
	**	value			The boolean value.
	**
	** History:
	**	 5-Sep-03 (gordy)
	**	    Created.
	**	 7-Dec-09 (gordy, ported by thoda04)
	**	    Added set method corresponding to transport format.
	*/

	internal class
		SqlBool : SqlData
	{

		private bool		Value = false;


		/*
		** Name: SqlBool
		**
		** Description:
		**	Class constructor.  Data value is initially NULL.
		**
		** Input:
		**	None.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	None.
		**
		** History:
		**	 5-Sep-03 (gordy)
		**	    Created.
		*/

		public
			SqlBool() : base( true )
		{
		} // SqlBool


		/*
		** Name: SqlBool
		**
		** Description:
		**	Class constructor which establishes a non-NULL value.
		**
		** Input:
		**	None.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	None.
		**
		** History:
		**	 5-Sep-03 (gordy)
		**	    Created.
		*/

		public
			SqlBool( bool _value ) : base( false )
		{
			this.Value = _value;
		} // SqlBool


		/*
		** Name: Set
		**
		** Description:
		**	Assign a new non-NULL data value.
		**
		** Input:
		**	value		The new data value.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	void.
		**
		** History:
		**	 5-Sep-03 (gordy)
		**	    Created.
		*/

		public void
			set( bool _value)
		{
			setNotNull();
			this.Value = _value;
			return;
		} // Set


		/*
		** Name: set
		**
		** Description:
		**	Assign a new non-NULL data value.
		**
		** Input:
		**	value		The new data value.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	void.
		**
		** History:
		**	 7-Dec-09 (gordy, ported by thoda04)
		**	    Created.
		*/

		public void
		set(byte _value)
		{
			setNotNull();
			this.Value = ((_value & 0x01) == 0x01);
			return;
		} // set


		/*
		** Name: set
		**
		** Description:
		**	Assign a new data value as a copy of an existing 
		**	SQL data object.  If the input is NULL, a NULL 
		**	data value results.
		**
		** Input:
		**	data	The SQL data to be copied.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	void.
		**
		** History:
		**	 1-Dec-03 (gordy)
		**	    Created.
		*/

		public void
		set(SqlBool data)
		{
			if (data == null || data.isNull())
				setNull();
			else
			{
				setNotNull();
				this.Value = data.Value;
			}
			return;
		} // set


		/*
		** Name: get
		**
		** Description:
		**	Return the current data value.
		**
		**	Note: the value returned when the data value is 
		**	NULL is not well defined.
		**
		** Input:
		**	None.
		**
		** Output:
		**	None.
		**
		** Returns:
		**	boolean	Current value.
		**
		** History:
		**	 1-Dec-03 (gordy)
		**	    Created.
		*/

		public bool
		get()
		{
			return (Value);
		} // get


		/*
		** Name: setBoolean
		**
		** Description:
		**	Assign a boolean value as the non-NULL data value.
		**
		** Input:
		**	value	Boolean value.
